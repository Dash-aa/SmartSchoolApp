package ua.edu.smartschool.model;

/** Перелік ролей користувачів у системі. Визначає права доступу та можливості користувача. */
public enum Role {

  /** Студент — має доступ до навчальних матеріалів та оголошень */
  STUDENT,

  /** Викладач — може створювати та редагувати навчальний контент */
  TEACHER,

  /** Адміністратор — має повний доступ до системи */
  ADMIN
}
